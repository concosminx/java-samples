package com.nimsoc.thread_executors_examples.results;

/**
 *
 * @author Cosminx
 */
public class ImResult {

  private String name;
  private int value;

  public ImResult(String name, int value) {
    this.name = name;
    this.value = value;
  }

  public String getName() {
    return name;
  }

  public int getValue() {
    return value;
  }

}
